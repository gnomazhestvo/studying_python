# 10.10. Распространенные слова.
# Зайдите на сайт проекта «Гутенберг» (http://gutenberg.org/)
# и найдите несколько книг для анализа. Скачайте текстовые
# файлы этих произведений или скопируйте текст из браузера
# в текстовый файл на вашем компьютере.
#
# Чтобы узнать, сколько раз слово или фраза встречается в
# строке, можно воспользоваться методом count(). Например,
# следующий код подсчитывает количество вхождений 'row' в
# строке:
# 			>>> line = "Row, row, row your boat"
# 			>>> line.count('row')
# 			>>> line.lower().count('row')
# Обратите внимание: преобразование строки в нижний
# регистр с помощью функции lower() позволяет найти все
# вхождения искомого слова независимо от регистра.
# 
# Напишите программу, которая читает файлы из проекта
# «Гутенберг» и определяет количество вхождений слова 'the'
# в каждом тексте. Результат будет приближенным, поскольку
# программа будет учитывать такие слова, как 'then' и 'there'.
# Попробуйте повторить поиск для строки 'the '
# (с пробелом в строке) и посмотрите, насколько уменьшится
# количество найденных результатов.

from pathlib import Path

print('10.10. Распространенные слова.\n')

def count_word(path):
    text = path.read_text()
    print(f'Слово "the" встречается в {path} {text.count('the')} раз (-а).')
    print(f'Слово "the " (с пробелом) - {text.count('the ')} раз (-а).')
    print(f'Всего слов - {len(text.split())}.\n')

filenames = ['text_files/alice.txt', 'text_files/moby_dick.txt',
             'text_files/little_women.txt']

for file in filenames:
    path = Path(file)
    count_word(path)