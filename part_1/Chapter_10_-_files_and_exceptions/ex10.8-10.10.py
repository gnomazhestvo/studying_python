from pathlib import Path

print('\n10.8, 10.9. Кошки и собаки и Ошибки без уведомления.\n')

path_cats = Path('text_files/cats.txt')
path_dogs = Path('text_files/dogs.txt')

path_cats.write_text('Борис\nТайсон\nМурзик')
path_dogs.write_text('Виссарион\nОбормот\nБармалей')

try:
    cats = path_cats.read_text().splitlines()
    print('Имена кошек: ', end='')
    for i in range(len(cats)):
        if i == len(cats)-1:
            print(f'{cats[i]}.')
        else:
            print(f'{cats[i]}, ', end='')
except(FileNotFoundError):
    pass

try:
    dogs = path_dogs.read_text().splitlines()
    print('Имена собак: ', end='')
    for i in range(len(dogs)):
        if i == len(dogs)-1:
            print(f'{dogs[i]}.')
        else:
            print(f'{dogs[i]}, ', end='')
except(FileNotFoundError):
    pass

